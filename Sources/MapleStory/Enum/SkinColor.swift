/// Skin Color
public enum SkinColor: UInt8, Codable, CaseIterable {
    
    case normal     = 0
    case dark       = 1
    case black      = 2
    case pale       = 3
    case blue       = 4
    case green      = 5
    case white      = 9
    case pink       = 10
}
